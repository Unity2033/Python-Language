# region 변수의 이름 규칙
# 1. 변수의 이름은 대소문자를 구분합니다.
# ex) health = 10
# ex) HEALTH = 20

# 2. 변수의 이름으로 예약어를 사용할 수 없습니다.
# ex) if = 3.141592

# 3. 변수의 이름에는 공백이 포함될 수 없습니다.
# ex) time delta = 0.016

# 4. 변수의 이름으로 특수 문자는 "_"만 사용할 수 있습니다.
# version_1 = 1

# 5. 변수의 이름은 숫자로 시작할 수 없습니다.
